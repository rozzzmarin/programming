#include <iostream>
using namespace std;

int main()
{
    int a, b;
    a = 3;
    b = 3.14;

    double c, d;
    c = 3;
    d = 3.14;

    cout << a << endl << b << endl << c << endl << d;

}
//  int - целочисленный тип данных, double - числа двойной точности, используются для представления числа с плавающей запятой
//  т.е. при преобразовании double в int b преобразовалась из 3.14 в 3